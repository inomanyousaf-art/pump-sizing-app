def get_pipe_id_in(pipe_size: str) -> float:
    """Return internal pipe diameter in inches for a nominal pipe size."""
    try:
        return PIPE_INTERNAL_DIAMETER_IN[pipe_size]
    except KeyError as exc:
        options = ", ".join(PIPE_INTERNAL_DIAMETER_IN)
        raise ValueError(f"Unknown pipe size {pipe_size!r}. Choose one of: {options}") from exc


def get_fitting_equivalent_length_ft(fitting_type: str, pipe_size: str) -> float:
    """Return equivalent length in feet for one fitting/valve."""
    try:
        return FITTING_EQUIVALENT_LENGTH_FT[fitting_type][pipe_size]
    except KeyError as exc:
        fitting_options = ", ".join(FITTING_EQUIVALENT_LENGTH_FT)
        pipe_options = ", ".join(PIPE_INTERNAL_DIAMETER_IN)
        raise ValueError(
            f"Unknown fitting or pipe size. "
            f"Fitting={fitting_type!r}, pipe_size={pipe_size!r}. "
            f"Fittings: {fitting_options}. Pipe sizes: {pipe_options}"
        ) from exc
